package employeemanagementsystemusingmap;

public class EmployeeData {
private int empId;
	private String empName; // Name of the Employee
	private String empDept; // Department of the Employee
	private String empAdr; // Address of the Employee
	private String empPN; // Phone Number of the Employee
	private String empDoB; // Date of birth of the Employee
	private String empSex; // Sex of the Employee
	private String empMS; // Marriage Status of the Employee
	private String empJT; // Job Title of the Employee
	private String empDsgn; // Designation of the EMployee

	public int getEmpId() {
		return empId;
	}

	public void setEmpId(int empId) {
		this.empId = empId;
	}

	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public String getEmpDept() {
		return empDept;
	}

	public void setEmpDept(String empDept) {
		this.empDept = empDept;
	}

	public String getEmpAdr() {
		return empAdr;
	}

	public void setEmpAdr(String empAdr) {
		this.empAdr = empAdr;
	}

	public String getEmpPN() {
		return empPN;
	}

	public void setEmpPN(String empPN) {
		this.empPN = empPN;
	}

	public String getEmpDoB() {
		return empDoB;
	}

	public void setEmpDoB(String empDoB) {
		this.empDoB = empDoB;
	}

	public String getEmpSex() {
		return empSex;
	}

	public void setEmpSex(String empSex) {
		this.empSex = empSex;
	}

	public String getEmpMS() {
		return empMS;
	}

	public void setEmpMS(String empMS) {
		this.empMS = empMS;
	}

	public String getEmpJT() {
		return empJT;
	}

	public void setEmpJT(String empJT) {
		this.empJT = empJT;
	}

	public String getEmpDsgn() {
		return empDsgn;
	}

	public void setEmpDsgn(String empDsgn) {
		this.empDsgn = empDsgn;
	}
}

